            	<h1>Uslovi putovanja</h1>
                <h3>Turistička agencija Dinamik Turs</h3>
                <p>Prijavljivanjem i uplatom akontacije za putovanje iz ovog programa zasniva se pravni odnos, ugovor o organizovanju putovanja između organizatora putovanja, "Dinamik-turs-a" i putnika koji stiče status učesnika putovanja uplatom cjelokupnog aranžmana. Odredbe opštih uslova putovanja su sastavni dio ovog ugovora koji određuje prava i obaveze ugovarača.</p>
           		
           		<article class="article">
                	
                
                    
                    <header>
                    	<h3><a href="#">1. PRIJAVE I UPLATE</a></h3>
                        
                    </header>
                    
                    <p>a) prijava putovanja obavlja se u vašoj putničkoj agenciji uz uplatu 40% vrijednosti aranžmana, a razlika do pune cijene aranžmana, uplaćuje se najkasnije 7 dana prije puta, osim u uslovima kada je druga dinamika plaćanja određena programom putovanja. Prilikom prijave potpisujete ugovor “Izjavljujem da su mi uslovi putovanja i osiguranja poznati i da ću ih u potpunosti poštovati”. Na taj način sve što je navedeno u ovim uslovima postaje pravna obaveza i za Vas i za nas; <br>
                    b) prijavljivanje se može vršiti putem pošte, a uplate odgovarajuće akontacije, sa naznakom za koga se uplaćuje, nazivom putovanja i datumom polaska, šalje na žiro račun, uz prethodnu konsultaciju sa prodajnim mjestom. U ovakvom slučaju organizator putovanja će putniku blagovremeno potvrditi da je njegova prijava prihvaćena;<br>
                    c) prilikom rezervacije „na upit” uplaćuje se akontacija: od 8 do 29 dana prije polaska 10% od cijene aranžmana po osobi; od 7 dana prije do dana polaska 20% od cijene aranžmana po osobi;<br>
                    d) u slučaju kreditnog odnosa primenjivaće se uslovi pod kojima kredit bude zasnovan.</p>
                </article>
           		<article class="article">
                	
                	
                    
                    <header>
                    	<h3><a href="#">2. CIJENE ARANŽMANA</a></h3>
                   
                    </header>
                    
                    <p>Cijene za sva putovanja date su po osobi na bazi smještaja u dvokrevetnim sobama. Za smještaj u jednokrevetnim sobama plaća se doplata, a za smještaj u višekrevetnim sobama odobrava se popust. Organizator putovanja zadržava pravo da izmeni cijenu aranžmana za odgovarajući iznos u sledećim slučajevima:
                    a) ako dođe do promene deviznog kursa koji je primenjen u izradi cijene aranžmana,<br>
                    b) ako dođe do promene u tarifama prevoznih, hotelskih i drugih usluga<br>
                    U oba slučaja promjene će se obračunati na ukupnu vrijednost aranžmana bez obzira na uplaćenu akontaciju. Za povećanje cijene aranžmana do 10% nije potrebna saglasnost putnika; u slučaju da putnik traži raskid ugovora, primjenjivaće se odredbe o otkazu putovanja po opštim uslovima putovanja. Ako povećanje cijene pređe 10%, putnik može raskinuti ugovor bez obaveze naknade štete organizatoru.</p>
                </article>
           		<article class="article">
                    
                    <header>
                    	<h3><a href="#">3. IZMJENE U PROGRAMU PUTOVANJA</a></h3>
                      
                    </header>
                    
                    <p>Organizator programa ima pravo da izvrši promenu programa usled više sile ili nastupanja okolnosti koje organizator nije mogao da predvidi ili otkloni (rat, nemiri, štrajkovi, terorističke akcije, sanitarni poremećaji, elementarne nepogode, intervencije nadležnih državnih vlasti i si.)<br>
Ugovoreni smještaj može se zamijeniti upotrebom objekata iste ili više kategorije na teret organizatora i u istom mjestu. Ako se u programu dogode bitne promene, organizator će vratiti putniku iznos primljenog novca u cjelini ukoliko putnik odustane od aranžmana prije početka putovanja. Ako bitne promjene nastanu za vreme izvršenja, putnik snosi troškove ostvarenih usluga do trenutka promjene programa.</p>
                </article>

                <article class="article">
                    
                    <header>
                        <h3><a href="#">4. PRAVO ORGANIZATORA NA OTKAZ </a></h3>
                      
                    </header>
                    
                    <p>Organizator može otkazati aranžman potpuno ili djelimično ako su nastale vanredne okolnosti koje se nisu mogle izbjeći ili otkloniti. Organizator može otkazati aranžman i kada se ne skupi dovoljan broj putnika predviđen za pojedine vrste aranžmana:<br>
• za aranžmane avionom posebno unajmljenim (čarter), 80% popunjenosti kapaciteta aviona;<br>
• za aranžmane autobusom 35 putnika;<br>
• za aranžmane na redovnim avionskim linijama 20 putnika; U slučaju otkaza iz navedenih razloga organizator će učesniku putovanja vratiti plaćenu sumu u cjelini. Organizator će postupiti identično i u slučaju da putnik nije u mogućnosti da prihvati novi datum polaska utvrđen zbog nastanka navedenih okolnosti.

</p>
                </article>

                <article class="article">
                    
                    <header>
                        <h3><a href="#">5. ODUSTAJANJE OD PUTOVANJA </a></h3>
                      
                    </header>
                    Ukoliko putnik želi da otkaže putovanje potrebno je da to učini isključivo u pismenoj formi. Putnik je dužan da u roku od 8 dana od dana otkazivanja dostavi potvrdu o nastanku naznačenih okolnosti izdatu od nadležnih organa. Datum pismenog otkaza predstavlja osnovu za obračun troškova otkaza i to:<br>
• za otkaz do 30 dana zadržava se 5% cijene arr po osobi;<br>
• za otkaz od 29 do 20 dana zadržava se 10% cijene arr po osobi;<br>
• za otkaz od 19 do 15 dana zadržava se 15% cijene arr po osobi;<br>
• za otkaz od 14 do 8 dana zadržava se 40% cijene arr po osobi;<br>
• za otkaz od 7 do 1 dana zadržava se 85% cijene arr po osobi;<br>
• za otkaz „rezervacije na upit” organizator zaračunava troškove otkaza u iznosu uplaćene akontacije (tačka 1c);<br>
• za nedolazak ili odustajanje nakon početka korišćenja aranžmana, zadržava se cijeli iznos cijene aranžmana, tj. uvećan do visine stvarnih troškova;<br>
• u slučaju da putnik promeni termin putovanja ili osigura zamenu na rezervisanom polasku, organizator će naplatiti 3% u KM od cijene aranžmana. Za slučaj nepredvidivih okolnosti (elementarne nepogode, vojne vježbe, smrtni slučaj, bolest) putnik je dužan da organizatoru podnese punovažne dokaze i to:<br>
a) za slučaj elementarne nepogode potvrdu štaba civilne zaštite;<br>
b) za slučaj poziva na vojnu vježbu odgovarajući dokument;<br>
c) za slučaj smrti u užoj porodici (roditelji, bračni drug, djeca) — umrlicu;<br>
d) za slučaj oboljenja u užoj porodici: ljekarsko uvjerenje zdravstvene ustanove na kojoj će od strane ljekara izričito biti navedeno da oboljela osoba ne može da koristi aranžman. U takvim slučajevima putnik ne plaća odštetu po navedenoj ljestvici već samo nadoknadu za otkaz ugovora. Za otkaz od 29 do 8 dana prije početka putovanja 10% cijene arr po osobi, za otkaz od 7 dana do dana polaskana put 20% cijene arr po osobi. Ako u ovim nepredvidivim okolnostima putnik otkaže već započeti aranžman naplatiće mu se iznos stvarnih troškova uz uslov da dostavi odgovarajuću potvrdu.
</p>
                </article>

                <article class="article">
                    
                    <header>
                        <h3><a href="#">6. OBAVEZE ORGANIZATORA</a></h3>
                      
                    </header>
                    
                    <p>Organizator je dužan da putniku pruži sve usluge navedene u katalogu za pojedini aranžman i odgovara putniku zbog eventualnog neizvršenja usluga ili dela usluga. Organizator isključuje svaku odgovornost u slučaju promena i neizvršenja usluga prouzro-kovanjih višom silom (tačka 3), kašnjenje prevoznih sredstava koja ne povlače odgovornost prevoznika prema pozitivnim zakonskim propisima i međunarodnim konvencijama.

                        </p>
                </article>
                <article class="article">
                    
                    <header>
                        <h3><a href="#">7. OBAVEZE PUTNIKA</a></h3>
                      
                    </header>
                    
                    <p>
                           Putnik je dužan da se lično stara da njegovi dokumenti i stvari ispunjavaju uslove predviđene graničnim, carinskim, zdravstvenim i drugim propisima kako naše zemlje tako i zemlje u koje putuje, da se pridržava kućnog reda u ugostiteljsko — hotelskim objektima, kao i da sarađuje sa predstavnikom organizatora i davaocima usluga; Za putovanje avionom potrebno je provjeriti vrijeme polijetanja dan prije polaska s obzirom na to da su moguće promene reda letenja.<br> U slučajevima kada Dinamik-turs obezbeđuje vize za boravak u određenoj zemlji, učesnik putovanja je dužan da dostavi pasoš i sva potrebna dokumenta blagovremeno. <br>Dinamik-turs ne snosi nikakvu odgovornost ukoliko diplomatsko-konzularno predstavništvo odbije izdavanje vize. U slučaju nedobijanja odgovarajuće vize, putnik snosi efektivne troškove otkaza.<br> Lica kojima nije odobrena ulazna viza na lični zahtjev nemaju izgleda da im viza bude odobrena ako zahtjev upute posredstvom agencije. U slučaju nepoštovanja ovih obaveza putnik odgovara organizatoru za učinjenu štetu, a organizator ne snosi odgovornost za štetu koju bi u tom slučaju mogao trpiti putnik.
                        </p>
                </article>
                <article class="article">
                    
                    <header>
                        <h3><a href="#">8. PRTLJAG</a></h3>
                      
                    </header>
                    
                    <p>
                             Besplatno se prevozi do 15 kg prtljaga na čarter letovima, a do 20 kg na redovnim linijama. Djeca do 2 godine nemaju pravo na besplatan prevoz prtljaga. Briga o prtljagu je obaveza prevoznika, tako da učesnik putovanja sva eventualna potraživanja zbog oštećenja ili gubitka prtljaga u toku prevoza može da ostvari obraćajući se direktno prevozniku.<br> Organizator putovanja ne odgovara za uništeni ili izgubljeni prtljag kao ni za krađu prtljaga i dragocjenosti u hotelu. U slučaju gubitka prtljaga putnik treba na licu mjesta da sačini zapisnik sa predstavnikom prevoznika i postupi prema ustanovljenim odredbama.


                        </p>
                </article>
                <article class="article">
                    
                    <header>
                        <h3><a href="#">9. ZDRAVSTVENI PROPISI</a></h3>
                      
                    </header>
                    
                    <p>
                               Za putovanje u pojedine zemlje, prema propisima Svjetske zdravstvene organizacije putnik je dužan da se vakciniše i pribavi odgovarajući dokument. Za sva interkontinentalna putovanja treba obavezno provjeriti da li postoji potreba za vakcinacijom ili prevencijom u zemljama u koje se putuje najkasnije 15 dana prije polaska. Vakcinisanje je potrebno i kada takav propis nastupi nakon zaključivanja ugovora o putovanju, pa ne može bit! razlog raskida ugovora ukoliko nije kontraindikovano putnikovom zdravlju. U tom slučaju putnik prilaže ljekarsko uverenje. Putnik ima pravo na zdravstvenu zaštitu u zemlji i inostranstvu u obimu i na način utvrđen propisima nadležne zdravstvene ustanove. Putnik je dužan da se upozna sa zdravstvenim propisima prije polaska na putovanje.
                        </p>
                </article>
                <article class="article">
                    
                    <header>
                        <h3><a href="#">10. REKLAMACIJE</a></h3>
                      
                    </header>
                    
                    <p>
                              Organizator će razmotriti samo reklamacije u pisanoj formi dostavljene najkasnije 8 dana po završetku putovanja i to ukoliko je učesnik putovanja upoznao vodiča Dinamik-turs-a sa predmetom reklamacije, neposredno po nastupanju okolnosti na koje se žali. Reklamaci je koje ne budu ispunjavale oba ova uslova neće se razmatrati. Dopis u dva prirnerka treba uputiti komercijalnoj službi.
                        </p>
                </article>
                 <article class="article">
                    
                    <header>
                        <h3><a href="#">11. VAŽNE NAPOMENE</a></h3>
                      
                    </header>
                    
                    <p>
                         Ukoliko učesnik putovanja svojevoljno prekine putovanje ili ako je sprečen da ga nastavi zbog učinjenog prekršaja, ili ako zbog nepoštovanja programa i uputstva vodiča (zakašnjenja i si.) ne koristi programom predviđene usluge, organizator neće prihvatiti zahtjev za vraćanje novca. Takođe nije moguće izdavanje duplikata izgubljene avionske ili bording karte. Ovaj program izdaje na sopstvenu odgovornost organizator putovanja i on se ne izdaje u ime prevoznika niti ga obavezuje. Ukoliko iz ugovora o organizovanju putovanja nastane spor, nadležan je sud u Brčkom.
                        </p>
